<h1 dir="ltr">3. Appropriate Leaders - how they behave</h1>

<p dir="ltr">There are certain behaviours that all ministry leaders should observe. When we follow these behaviours, it will greatly reduce the likelihood of inappropriate behavior and/or abuse occurring at your church. But also, adhering to these behaviours will help reduce the likelihood of false allegations being made against leaders like you, as well as the church.</p>

<p dir="ltr">So how do should we behave and run safe ministries?</p>

<ol>
	<li dir="ltr">
	<p dir="ltr">Stay where others can see you<br />
	The core to leading safe ministries is to always be within eyesight of other people. Incidents of abuse are much less likely to happen when there are other people around.<br />
	This is especially true with children. Never be alone with a child, and never allow other people to be alone with a child. This is also true of leading vulnerable adults - always be where other people can easily see you.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Model appropriate physical contact<br />
	It&rsquo;s important that you understand what type of physical contact is, and is not, appropriate for the ministry you&rsquo;re doing. Appropriate physical contact will depend on a person&rsquo;s age;</p>

	<ol>
		<li dir="ltr">
		<p dir="ltr">It&rsquo;s appropriate that babies and young toddlers be picked-up and cuddled.</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">Children aged 1-3 may require physical assistance for tasks, however leaders should avoid having children on laps, or any extended physical contact.</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">With children aged 4-6, unless there&rsquo;s an emergency, leaders should ask the child&rsquo;s permission if they need physical assistance. Even though it&rsquo;s common for these children to initiate hugs and other contact; leaders should quickly redirected such children to other activities.</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">With anyone over 6 years old, its almost always best to avoid physical contact. It is fine to high-five, handshake, side-hug. But again, avoid prolonged contact.</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">Also, if you&rsquo;re playing games which involve physical contact, carefully consider how to reduce the chance of unsafe or inappropriate contact between individuals and/or leaders.</p>
		</li>
	</ol>
	</li>
	<li dir="ltr">
	<p dir="ltr">Model appropriate interactions<br />
	As a potential leader, you need to consider how you interact with others, both verbally and nonverbally. Avoid all crude language and sexually explicit joking; these are commonly mistaken as flirting or advances. Be careful discussing overly private matters outside the scope of your ministry. Also, be careful to dress modestly, keeping yourself private at all times.<br />
	People often give leaders great influence over their lives. Therefore, leaders must be very careful about offering advice and suggestions, as these can be heard as instructions. Leaders should never use their position of authority to achieve their own personal gain.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Provide safe ministry spaces<br />
	When it comes to the places you&rsquo;re running ministry activities, you need to think carefully.<br />
	Ministry spaces should be clean and safe to use. You should know where to quickly find first aid, and where to gather if there&rsquo;s an emergency.<br />
	If there&rsquo;s someone hanging around a ministry space when they don&rsquo;t need to be there, feel free to ask them to leave the space.<br />
	If you&rsquo;re involved in runnings camps, weekends away or you need to use change rooms, you must first get advice from a Safe Ministry Supervisor at your church.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Appropriate transportation<br />
	If you ever need to transport people, be sure to get appropriate permission from parents/guardians. Leaders should obey all road rules and only use suitable vehicles (that is, make sure they&rsquo;re registered and in good working order). You must take direct routes to and from the locations so you can not be accused of any wrongdoing along the way.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Toileting procedures<br />
	Lastly, know your church&rsquo;s policy about toileting young children. Usually, it is best to ask parents to take their children to the toilet. However, If leaders need to take children to the toilet: This is usually only be done by females who should try to take a group of children to the toilet (so they do not end up being left alone with a child).</p>
	</li>
</ol>

<p dir="ltr">There&rsquo;s a lot to remember there, so as you&rsquo;re involved in leading programs, keep asking others for feedback on how to behave appropriately.</p>

