<h1 dir="ltr">4. Inappropriate Incidents - How to identify them</h1>

<p dir="ltr">It is a sad fact that many people (children and adults) have been the victim of abuse or inappropriate behaviour.</p>

<p dir="ltr">Abuse can arise in various forms: Sexual Abuse, Physical Abuse, Emotional Abuse and Neglect.</p>

<p dir="ltr">As a potential leader at your church, you should be able to identify the signs of an abuse victim, and respond appropriately.</p>

<p dir="ltr">In this section, we&rsquo;ll just look at how to identify possible forms of abuse.</p>

<ul>
	<li dir="ltr">
	<p dir="ltr">Physical Abuse<br />
	Physical abuse is when a child or adult has been injured from a non-accidental act. This injury might be caused by being hit, punched, kicked, shaken, burnt, bit, or from having their hair-pulled. The signs of such physical abuse include bruising, lacerations or welts, burns, fractures or dislocation of joints.<br />
	Another sign of physical abuse could also be when an explanation of an &ldquo;accident&rdquo; is not consistent with the injury you can see.<br />
	Please note that a crime has been committed if a child has been hit around the head or neck. It is also a crime if a stick, belt or other object has been used to discipline or punish a child.<br />
	If someone shows one or more of these signs, they could have experienced physical abuse.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Sexual abuse<br />
	Sexual abuse is when one person uses their power (or authority) to take advantage of another person&rsquo;s trust and involves them in a sexual activity. Both children and adults can be sexually abused and it does not necessarily involve genital contact.<br />
	Signs of sexual abuse can be:</p>

	<ul>
		<li dir="ltr">
		<p dir="ltr">Adolescent pregnancy</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">bruising or bleeding in the genital area, breasts, buttocks, stomach or thighs</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">Signs of sexual abuse can also include a child describing sexual acts or having a inappropriately detailed understanding of sex for their age</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">regressive behaviour like bed-wetting, or going to be fully clothed</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">Self-harm, self-loathing or persistent running away from home</p>
		</li>
	</ul>
	</li>
</ul>

<p dir="ltr" style="margin-left: 40px;">Sexual abuse is a crime. If a child or adult displays one or more of these signs, they could have experienced sexual abuse.</p>

<ul>
	<li dir="ltr">
	<p dir="ltr">Emotional abuse<br />
	Emotional abuse is when, usually over an extended time, a child or adult is excessively criticised, withheld affection, exposed to domestic-violence, intimidated or threatened. The possible signs that a child or adult being emotionally abused are;</p>

	<ul>
		<li dir="ltr">
		<p dir="ltr">an inability to value or trust others,</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">either extremely aggressive or extremely eager to please,</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">constant feelings of worthlessness,</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">persistent running away from home.</p>
		</li>
	</ul>
	</li>
</ul>

<p dir="ltr" style="margin-left: 40px;">If a child or adult displays one or more of these signs they may be experiencing emotional abuse.</p>

<ul>
	<li dir="ltr">
	<p dir="ltr">Neglect and homelessness<br />
	Neglect is when a child or adult does not have their basic needs met, like shelter, food, clothing. Signs that a child or adult is experiencing neglect or homelessness are:</p>

	<ul>
		<li dir="ltr">
		<p dir="ltr">extremely poor personal hygiene,</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">extreme hunger,</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">a reluctance to go home,</p>
		</li>
		<li dir="ltr">
		<p dir="ltr">always arriving early.</p>
		</li>
	</ul>
	</li>
</ul>

<p dir="ltr" style="margin-left: 40px;">If a child or adult displays one or more of these signs, they may be experiencing neglect and/or homelessness.</p>

<p dir="ltr">These are certainly dreadful things to happen to people. However, they do happen, and one of the best ways to avoid them happening in your church is to be aware of them and be able to identify them.</p>

