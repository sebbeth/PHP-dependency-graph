<h1 dir="ltr">5. Inappropriate Incidents - How to respond to them</h1>

<p dir="ltr">As a potential leader at your church, you may be in a situation where you suspect a child or adult is experiencing abuse. This may be because of things you&rsquo;ve observed, or the victim might choose to tell you they&rsquo;re experiencing abuse.</p>

<p dir="ltr">So, what should you NOT do if a child or adult at your church tells you they are experiencing abuse?</p>

<ol>
	<li dir="ltr">
	<p dir="ltr">DO NOT make any promises. People who have experienced abuse have already had their trust destroyed. So don&rsquo;t promise that you won&rsquo;t tell anyone. In the same way, don&rsquo;t promise that the abuse will stop. Just say, &ldquo;I&rsquo;ll work out the best thing to do.&rdquo;</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">DO NOT ask &ldquo;leading questions&rdquo;. Leading questions are when we ask things like, &ldquo;You&rsquo;re saying he hurt you when he did that?&rdquo; They are questions that are phrased in a way that suggests a certain answer. If the incident ends up with legal authorities involved, leading questions like that can make it difficult for police to press charges against abusers.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">DO NOT contact the person who you suspect of inflicting the abuse.<br />
	If there is truth to the accusation and the abuser thinks they have been discovered, they not only can make legal action even more difficult, but they can also bring further danger to the victim.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">DO NOT contact the parents of a child you suspect has been abused, .<br />
	There have been a few cases where the parents have not believed their children and caused further abuse, or the parents are involved in the abuse. Even if this seems unbelievable to you, you are not the best person to contact the child&rsquo;s parents about this.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">DO NOT speak about this to anyone else other than the Safe Ministry Supervisors.<br />
	These types of accusations have the power to destroy careers and even whole churches. There have been some instances where false and malicious accusations have been made, and even though they were proved false, the stigma of the accusation destroyed people&rsquo;s lives. So don&rsquo;t mention it to anyone else.</p>
	</li>
</ol>

<p dir="ltr">So, what should you do if you suspect a child or adult at your church is experiencing abuse?</p>

<ol>
	<li dir="ltr">
	<p dir="ltr">Stay calm and love them by listening carefully. Tell them that you are listening and you are there to give them support.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Write it down<br />
	Whether you&rsquo;ve had a conversation or observed potential signs of abuse, write a record of as much of the conversation or the signs as you can remember. Include the date, location and time of the conversation. Again, if any authorities need to get involved, this initial record becomes very important.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">You should immediately raise the matter with one of the Safe Ministry Supervisors at your church.<br />
	The Safe Ministry Supervisors at your church understand all the appropriate steps and procedures to take if there&rsquo;s an incident or accusation. Depending on the details, the Safe Ministry Supervisor may contact Family and Community Services or the Police and take appropriate action. You also may be required to provide a statement to the Police and/or to Family and Community Services if it is confirmed that a child is at risk.<br />
	(Obviously, if the accusation is against one of the Safe Ministry Supervisors, you should go to one of the other Safe Ministry Supervisors at your church).</p>
	</li>
</ol>

<p>One last note: If someone tells you that they think another person is experiencing abuse, you can not report that. It&rsquo;s secondhand information. Rather, the best thing is to take them to the Safe Ministry Supervisor so they can to report what they heard themselves.</p>

