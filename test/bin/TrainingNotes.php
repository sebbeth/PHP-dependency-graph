<h1 dir="ltr">1. Introduction</h1>

<p dir="ltr">Welcome to the Safe Ministry Training course. We all want our churches to be safe places for everyone - kids as well as adults. The purpose of this training is to explain the basic principles and processes which you and your church will take to ensure we all provide safe ministries.</p>

<p dir="ltr">There&rsquo;s four main areas of this training, and each area has its own short video and quiz. We include the quiz to make sure everyone understands this important information.</p>

<ul>
	<li dir="ltr">
	<p dir="ltr">First, you&rsquo;ll see how your church is going to make sure it only has Appropriate leaders and volunteers serving in ministry teams.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Second, you&rsquo;ll learn what is and is not appropriate behaviour as a volunteer at your church.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Third, you&rsquo;ll learn how to spot signs that someone&rsquo;s been treated inappropriately.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">And lastly, how you should respond if you think someone&rsquo;s been treated inappropriately.</p>
	</li>
</ul>

<p dir="ltr">It&rsquo;s worth saying that because of the explicit nature of some of the material covered in this training, some people may find it disturbing.</p>

<p dir="ltr">&nbsp;</p>

<h1 dir="ltr">2. Screening, Training &amp; Appointing of Appropriate Leaders</h1>

<p dir="ltr">The first aspect of providing safe ministries at your church is to make sure that only appropriate people are involved in leading those ministries.</p>

<p dir="ltr">Now, when we use the term &ldquo;leader&rdquo;, we don&rsquo;t just mean the Pastor or the Head of the Sunday School. For insurance purposes, a &ldquo;leader&rdquo; is anyone who is appointed to a position of authority over others. That could be a Bible Study Leader, a Sunday School helper, a Band leader, an Event Organiser. They&rsquo;re all in leadership.</p>

<p dir="ltr">So what makes an appropriate leader? Well, it&rsquo;s really just 3 things&hellip;</p>

<ol>
	<li dir="ltr">
	<p dir="ltr">First, appropriate leaders are people who&rsquo;ve been a regular part of your church - usually for more than 6 months. There might be some exceptions to this, which your church leadership can sort out. Before leaders start, they&rsquo;ll be interviewed and have their potential roles and responsibilities clearly outlined for them.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Second, appropriate leaders are people who have had reference checks to confirm they have the appropriate character to be involved in leadership at your church.<br />
	This also means, if they&rsquo;re going to be working with children, they also have the appropriate legal approval.<br />
	So, if you&rsquo;re in NSW, that means providing your Working with Children Check Number. Your church will confirm with the appropriate authority that you are approved to work with children.<br />
	No one with a criminal record of child abuse, molestation or similar offences can be involved in any ministry in your church to people under 18 years old.</p>
	</li>
	<li dir="ltr">
	<p dir="ltr">Third, appropriate leaders are people who have completed a Safe Ministry Training Course like this one. This material should be reviewed every 2-3 years.</p>
	</li>
</ol>

